			<h3>（一）客体要件</h3>
			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本罪侵犯的客体，是复杂客体。其中，次要客体是国家工作人员职务行为的廉洁性；主要客体是国家机关、国有公司、企事业单位、人民团体的正常管理活动。
			<h3>（二）客观要件</h3>
			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本罪在客观方面表现为行为人具有利用职务上的便利，向他人索要性服务，或者接受他人性服务并为他人谋取利益的行为。<br/>
			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;利用职务之便是受贿罪客观方面的一个重要构成要件，利用职务之便可以分为以下两种情况：<br/>
			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（1）利用职务上的便利。职权是指国家机关及其公职人员依法作出一定行为的资格，是权力的特殊表现形式。具体是指利用本人职务范围内的权力，也即利用本人在职务上直接处理某项事务的权利。在司法实践中，大量受贿罪是利用职权的便利条件构成的。例如，负责掌管物资调拨、分配、销售、采购的人，利用其调拨权、分配权、销售采购权，满足行贿人的愿望，而接受他人性服务。<br/>
			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（2）利用与职务有关的便利条件。利用与职务有关的便利，即不是直接利用职权，而是利用本人的职权或地位形成的便利条件，而本人从中向请托人索取或接受他人性服务行为。实践中，利用第三者职务上的便利，主要有以下三种情况：一是亲属关系，二是私人关系，三是职务关系。至于前两种情况，利用的主要是血缘与感情的关系，与本人职务无关。对于单纯利用亲友关系，为请托人办事，从中接受他人性服务的，不应以受贿论处。在第三种情况下，则与本人职务有一定关联。受贿人利用第三者的职务之便受贿，必须具备以下两个条件，其一，利用第三者的职务之便，必须以自已的职务为基础或者利用了与本人职务活动有紧密联系的身份便利。其二，是受贿人从中周旋使他人获得利益。根据司法实践，利用与职务有关的便利条件，一般发生在职务上存在制约或者相互影响关系的场合。从受贿罪的客观行为来看，有两种具体表现形式：<br/>
