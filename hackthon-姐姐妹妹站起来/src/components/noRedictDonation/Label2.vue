			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;（2）行为人利用职务上的便利，接受他人性服务并为他人谋取利益的行为。<br/>
			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;传统观点认为，为他人谋取利益是受贿罪的客观要件，如果国家工作人员收受财物但事实上并没有为他人谋取利益的，则不成立受贿罪。同时认为，为他人谋取的利益是否已经实现，不影响受贿罪的成立。最高人民法院、最高人民检察院1989年《关于执行〈关于惩治贪污罪贿赂罪的补充规定〉若干问题酌解答》也指出：非法收受他人财物，同时具有‘为他人谋取利益的，才能构成受贿罪。为他人谋取的利益是否正当，为他人谋取的利益是否实现，不影响受贿罪的成立。据此，为他人谋取利益，是指客观上有为他人谋取利益的行为，而不要求实际上使他人取得了利益。我们将这种观点称为旧客观说。旧客观说存在许多问题，如与受贿罪的本质不相符合，与认定受贿既遂的标准不相符合，与罪刑相适应原则不相符合，于是有人提出，为他人谋取利益不是客观要件，而是主观要件（主观要件说）。但这种观点对刑法规定进行了扭曲解释，也容易不当地缩小受贿罪的处罚范围。因此，我们认为，为他人谋取利益仍然是受贿罪的客观要件，其内容是许诺为他人谋取利益。国家工作人员在非法收受他人财物之前或者之后许诺为他人谋取利益，就在客观上形成了以权换利的约定，同时使人们产生以下认识：国家工作人员的职务行为是可以收买的，只要给予利益，就可以使国家工作人员为自己谋取各种利益。这本身就使职务行为的不可收买性受到了侵犯。这样理解，也符合刑法的规定：为他人谋取利益的许诺本身是一种行为，故符合刑法将其规定为客观要件的表述；为他人谋取利益只要求是一种许诺，不要求有谋取利益的实际行为与结果；为他人谋取利益只是一种许诺，故只要收受了财物就是受贿既遂，而不是待实际上为他人谋取利益之后才是既遂。<br/>
			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;为他人谋取利益的许诺本身是一种行为。许诺既可以是明示的，也可以是暗示的。当他人主动行贿并提出为其谋取利益的要求后，国家工作人员虽没明确答复办理，但只要不予拒绝，就应当认为是一种暗示的许诺。许诺既可以直接对行贿人许诺，也可以通过第三者对行贿人许诺。许诺既可以是真实的，也可以是虚假的。虚假许诺，是指国家工作人员具有为他人谋取利益的职权或者职务条件，在他人有求于自已的职务行为时，并不打算为他人谋取利益，却又承诺为他人谋取利益。但虚假承诺构成受贿罪是有条件的：其一，一般只能在接受他人性服务后作虚假承诺；其二，许诺的内容与国家工作人员的职务有关联；其二，因为许诺而在客观上形成了为他人谋取利益的约定。<br/>
