			<h2><li>定义</li></h2>
			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;性贿赂包含性受贿和性行贿两个对行性的罪名。性受贿可以定义为“国家工作人员利用职务上的便利接受他人提供非正当性服务为他人谋取利益”，而性行贿则可以定义为“谋取不正当利益，给予国家工作人员以不正当性服务”中国刑法第385条第一款对受赂罪的定义是“国家工作人员利用职务上的便利，索取他人财物的，或者非法收受他人财物，为他人谋取利益的，是受贿罪。”第389条第一款对行贿罪的规定是“为谋取不正当利益，给予国家工作人员以财物的，是行贿罪。”这两条都是针对财物性贿赂犯罪做出的规定，但是贿赂的形式却远远不止财物一种，按照这两款规定，其他贿赂形式是不能按照贿赂犯罪来定罪量刑的。这就为那些利用美色进行贿赂犯罪的犯罪分子钻空子提供了可乘之机。对性贿赂作出规定可以有效地堵上这一个法律漏洞。
